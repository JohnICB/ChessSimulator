package ro.uaic.info.ip.proiect.b3.controllers.subject.Objects;

public class Tema {
    String titluMaterie;
    String deadline;
    String enunt;
    Integer nrExercitii;
    String extensieFisier;
    String numeTema;

    public String getTitluMaterie() {
        return titluMaterie;
    }

    public void setTitluMaterie(String titluMaterie) {
        this.titluMaterie = titluMaterie;
    }

    public String getDeadline() {
        return deadline;
    }

    public void setDeadline(String deadline) {
        this.deadline = deadline;
    }

    public String getEnunt() {
        return enunt;
    }

    public void setEnunt(String enunt) {
        this.enunt = enunt;
    }

    public Integer getNrExercitii() {
        return nrExercitii;
    }

    public void setNrExercitii(Integer nrExercitii) {
        this.nrExercitii = nrExercitii;
    }

    public String getExtensieFisier() {
        return extensieFisier;
    }

    public void setExtensieFisier(String extensieFisier) {
        this.extensieFisier = extensieFisier;
    }

    public String getNumeTema() {
        return numeTema;
    }

    public void setNumeTema(String numeTema) {
        this.numeTema = numeTema;
    }
}
